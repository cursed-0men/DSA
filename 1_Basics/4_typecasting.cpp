// Typescasting
// Converting from one datatype to another.

#include<iostream>
using namespace std;
int main()
{

    int a = 'c';
    cout<<a<<endl; 
    // here c is being typecasted into 'int'.
    // it prints ASCII value of 'c'.      
    
    char ch = 98;
    cout<<ch<<endl;
    // prints b.
    // 98 is being typecasted.
return 0;
}